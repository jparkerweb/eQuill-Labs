import { curate } from '../../scripts/curate.ts';
import fs from 'node:fs';
const snap = JSON.parse(fs.readFileSync('data/github-snapshot.json','utf8'));
const feat = JSON.parse(fs.readFileSync('site/featured.json','utf8'));
const man  = JSON.parse(fs.readFileSync('data/build-manifest.json','utf8'));
const curated = curate(snap, feat);
const newSlugs = curated.map((c: any)=>c.slug);
const oldSlugs = (man.projects||[]).map((p: any)=>p.slug);
const oldSet = new Set(oldSlugs);
const newSet = new Set(newSlugs);
const added = newSlugs.filter((s: string)=>!oldSet.has(s));
const removed = oldSlugs.filter((s: string)=>!newSet.has(s));
let reordered = 0;
const commonNew = newSlugs.filter((s: string)=>oldSet.has(s));
const commonOld = oldSlugs.filter((s: string)=>newSet.has(s));
for (let i=0;i<commonNew.length;i++) if(commonNew[i]!==commonOld[i]) reordered++;
const featuredCount = curated.filter((c: any)=>c.featured).length;
const pinnedCount = curated.filter((c: any)=>c.pinned).length;
const hiddenByOverrides = snap.repos.length - curated.length;
console.log('CURATED:', curated.length);
console.log('FEATURED:', featuredCount, 'PINNED:', pinnedCount, 'HIDDEN:', hiddenByOverrides);
console.log('ADDED:', added.length, JSON.stringify(added));
console.log('REORDERED:', reordered);
console.log('REMOVED:', removed.length, JSON.stringify(removed));
